let currentLanguage = 'fr';

function changeLanguage(lang) {
    currentLanguage = lang;
    document.documentElement.lang = lang;
    updateContent();
}

function updateContent() {
    document.querySelectorAll('[data-i18n]').forEach(element => {
        const keys = element.getAttribute('data-i18n').split('.');
        let value = translations[currentLanguage];
        keys.forEach(key => {
            value = value[key];
        });
        element.textContent = value;
    });
    updateTeamRoles();
}

function updateTeamRoles() {
    const roles = translations[currentLanguage].team.roles;
    document.querySelectorAll('[data-member-role]').forEach(element => {
        const roleKey = element.getAttribute('data-member-role');
        if (roles[roleKey]) {
            element.textContent = roles[roleKey];
        }
    });
}

// Initialize with French
document.addEventListener('DOMContentLoaded', () => {
    updateContent();
}); 
